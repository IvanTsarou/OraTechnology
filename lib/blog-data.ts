export type DatePreset = "today" | "week" | "month" | "year" | "all"

export function filterPostsByDate(posts: BlogPost[], preset: DatePreset): BlogPost[] {
  if (preset === "all") return posts

  const now = new Date()
  const cutoff = new Date()

  switch (preset) {
    case "today":
      cutoff.setHours(0, 0, 0, 0)
      break
    case "week":
      cutoff.setDate(now.getDate() - 7)
      break
    case "month":
      cutoff.setMonth(now.getMonth() - 1)
      break
    case "year":
      cutoff.setFullYear(now.getFullYear() - 1)
      break
  }

  return posts.filter((p) => new Date(p.date) >= cutoff)
}
