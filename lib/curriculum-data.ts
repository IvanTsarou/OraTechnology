export function getCoursesByMonth(monthKey: string): CurriculumCourse[] {
  return curriculumCourses
    .filter((c) => c.dateStart.startsWith(monthKey))
    .sort((a, b) => a.dateStart.localeCompare(b.dateStart))
}

export function getCourseById(id: string): CurriculumCourse | undefined {
  return curriculumCourses.find((c) => c.id === id)
}

export function formatDateRange(dateStart: string, dateEnd?: string): string {
  const start = new Date(dateStart)
  const dayStart = start.getDate().toString().padStart(2, "0")
  const monthStart = (start.getMonth() + 1).toString().padStart(2, "0")

  if (!dateEnd || dateEnd === dateStart) {
    return `${dayStart}.${monthStart}`
  }

  const end = new Date(dateEnd)
  const dayEnd = end.getDate().toString().padStart(2, "0")
  const monthEnd = (end.getMonth() + 1).toString().padStart(2, "0")

  if (monthStart === monthEnd) {
    return `${dayStart}–${dayEnd}.${monthStart}`
  }
  return `${dayStart}.${monthStart}–${dayEnd}.${monthEnd}`
}
