export function getLevelLabel(level: StudentLevel): string {
  return studentLevels.find((l) => l.id === level)?.label ?? level
}

export function getCategoryById(id: string): CurriculumCategory | undefined {
  return curriculumCategories.find((c) => c.id === id)
}

export function getTeacherById(id: string): CurriculumTeacher | undefined {
  return curriculumTeachers.find((t) => t.id === id)
}

